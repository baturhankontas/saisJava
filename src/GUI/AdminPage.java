    private void DosyayiGuncelle(ArrayList<User> kullaniciListesi, ArrayList<Course> dersListesi) throws FileNotFoundException, IOException
    {
        File fout = new File("veriler.txt");
        FileOutputStream fos = new FileOutputStream(fout);
        BufferedWriter bw = new BufferedWriter(new OutputStreamWriter(fos));
        for (int sayac = 0; sayac < kullaniciListesi.size(); sayac++) {
            Object o = kullaniciListesi.get(sayac);
            if (o.getClass().equals(Student.class))
            {
                Student s = (Student)o;
                bw.write("S"+"~"+s.getName()+"~"+s.getSurname()+"~"+s.getTC()+"~"+s.getID()+"~"+s.getPassword()+"~"+s.getBirthYear());
                for (int sayac2 = 0; sayac2 < s.getDersler().size(); sayac2++) {
                    bw.write("~" + s.getDersler().get(sayac2).getCode()+"~"+s.getDersler().get(sayac2).getName());
                }
                bw.newLine();
            }
            else if (o.getClass().equals(Admin.class))
            {
                Admin s = (Admin)o;
                bw.write("A"+"~"+s.getName()+"~"+s.getSurname()+"~"+s.getTC()+"~"+s.getID()+"~"+s.getPassword());
                bw.newLine();
            }
            else
            {
                Teacher s = (Teacher)o;
                bw.write("T"+"~"+s.getName()+"~"+s.getSurname()+"~"+s.getTC()+"~"+s.getID()+"~"+s.getPassword());
                bw.newLine();
            }
        }
        if (dersListesi.size() > 0) {
            bw.write("C");
            for (int sayac = 0; sayac < dersListesi.size(); sayac++) {
            bw.write("~"+dersListesi.get(sayac).getCode()+"~"+dersListesi.get(sayac).getName()+"~"+dersListesi.get(sayac).isStatus());
            }
        }
        bw.close();
    }

