    private void btnAdminSecActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdminSecActionPerformed
        String kisiAdi = cmBoxAdminOgrenciSec.getSelectedItem().toString();
        for (int sayac = 0; sayac < alinacakDersler.size(); sayac++) {
            Course2Admin c = alinacakDersler.get(sayac);
            if (kisiAdi.equals(c.getOgrenci().getName()+" "+c.getOgrenci().getSurname())) {
                
                for (int sayac2 = 0; sayac2 < c.getDersler().size(); sayac2++) {
                    listModel.addElement(c.getDersler().get(sayac2).getName());
                    listAdmin.setModel(listModel);
                }
            }
        }
    }//GEN-LAST:event_btnAdminSecActionPerformed

    private void btnAdminDersOnaylaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdminDersOnaylaActionPerformed
        for (int sayac = 0; sayac < alinacakDersler.size(); sayac++) {
            Course2Admin c = alinacakDersler.get(sayac);
            if(cmBoxAdminOgrenciSec.getSelectedItem().toString().equals(c.getOgrenci().getName()+" "+c.getOgrenci().getSurname()))
            {
                Student s = c.getOgrenci();
                for (int sayac2 = 0; sayac2 < c.getDersler().size(); sayac2++) {
                    s.dersEkle(c.getDersler().get(sayac2));
                }
                alinacakDersler.remove(sayac);
                break;
            }
        }
        listModel.removeAllElements();
        listAdmin.setModel(listModel);
        cmBoxAdminOgrenciSec.removeAllItems();
        for (int sayac = 0; sayac < alinacakDersler.size(); sayac++) {
            cmBoxAdminOgrenciSec.addItem(alinacakDersler.get(sayac).getOgrenci().getName()+ " " + alinacakDersler.get(sayac).getOgrenci().getSurname());
        }
        JOptionPane.showMessageDialog(null, "\n" + 	"Course Approved");
    }//GEN-LAST:event_btnAdminDersOnaylaActionPerformed

